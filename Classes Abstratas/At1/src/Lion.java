class Lion extends Animal {
    @Override
    public void respirar() {
        System.out.println("O leão está respirando.");
    }

    @Override
    public void dormir() {
        System.out.println("O leão está dormindo.");
    }

    @Override
    public void comer() {
        System.out.println("O leão está comendo.");
    }

    @Override
    public void andar() {
        System.out.println("O leão está andando.");
    }
}
